package com.alexg.zone.toyrobot.grid.dimension;

public class GridDimensions {
    private final int maxYPosition = 4;
    private final int maxXPosition = 4;
    private final int minYPosition = 0;
    private final int minXPosition = 0;

    public int getMaxXPosition() {
        return maxXPosition;
    }

    public int getMaxYPosition() {
        return maxYPosition;
    }

    public int getMinXPosition() {
        return minXPosition;
    }

    public int getMinYPosition() {
        return minYPosition;
    }
}
